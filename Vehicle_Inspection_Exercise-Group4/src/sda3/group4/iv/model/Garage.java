package sda3.group4.iv.model;

/**
 * Handles the garage functionalities.
 */
public class Garage {

    /**
     * Opens the garage door.
     */
    public void open(){
        System.out.println();
        System.out.println("*The door is now open*");
    }

    /**
     * Closes the garage door.
     */
    public void close(){
        System.out.println("*The door is now closed*");
        System.out.println();
    }
}
